namespace CrossX.Graphics2D.Text
{
    /// <summary>
    /// Defines text alignment.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Text should aligned to the left.
        /// </summary>
        Left,
        /// <summary>
        /// Text should be centered.
        /// </summary>
        Center,
        /// <summary>
        /// Text should be aligned to the right.
        /// </summary>
        Right,
        /// <summary>
        /// Text should be aligned to the left and justified.
        /// </summary>
        Justify
    }
}
